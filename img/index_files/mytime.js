$(function() {
    $(".imgone").hover(function() {
        var word = $(this).attr("data-title");
        $(".tipone em").text(word);
        // var pos=$(this).offset().left;
        $(".tipone").css({
            "opacity": 1
        });
    }, function() {
        $(".tipone").css({
            "opacity": 0
        });
    })
})

