$(function() {
    $(".imgimgone").hover(function() {
        var word = $(this).attr("data-title");
        $(".tipnavone em").text(word);
        // var pos=$(this).offset().left;
        $(".tipnavone").css({
            "opacity": 1
        });
    }, function() {
        $(".tipnavone").css({
            "opacity": 0
        });
    })
})

